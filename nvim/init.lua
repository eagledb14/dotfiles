-- toggle terminal functions
function ToggleTerminal()
  local ok, _ = pcall(function() vim.cmd('bd! term') end)
  if not ok then
    vim.cmd('vsplit')
    vim.cmd('term')
    vim.cmd('vertical resize 50')
    vim.cmd('startinsert')
    -- vim.cmd('wincmd p')
  end
end

